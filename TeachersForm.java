    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed

        try {
 
            int selectTeacher = jTable2.getSelectedRow();

            if (selectTeacher == -1 || SharedSelection.selectedSubjectId == -1) {
                JOptionPane.showMessageDialog(this, "Please select a teacher and a subject first!");
                return;
            } 
            int tid = Integer.parseInt(jTable2.getValueAt(selectTeacher, 0).toString());
            int subjid = SharedSelection.selectedSubjectId;
            
            if (x.oneTOone(tid, subjid)) {
            JOptionPane.showMessageDialog(this, "Teacher or subject already assigned!");
            return;
        }

            // confirmation dialog
            int confirm = JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to assign this teacher to subject: " + SharedSelection.selectedSubjectCode + "?",
                "Confirm Enrollment",
                JOptionPane.YES_NO_OPTION
            );

            if (confirm == JOptionPane.YES_OPTION){
                Assign assign = new Assign();
                assign.setSubid(subjid);
                String message = assign.assignSubj(tid);
                JOptionPane.showMessageDialog(this, message);

                // refresh enrolled subjects
                ShowAssignRec(tid);
            } else {
                JOptionPane.showMessageDialog(this, "Assignment canceled.");
            }

        }  catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        try {
            int tid = Integer.parseInt(teachID.getText()); // your student id field
            int row = jTable3.getSelectedRow();

            if (row < 0) {
                JOptionPane.showMessageDialog(this, "Please select a subject to delete first!");
                return;
            }

            int subjid = Integer.parseInt(jTable3.getValueAt(row, 0).toString());
            x.setSubid(subjid);
            
            int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to unassign this teacher to subject: " + SharedSelection.selectedSubjectCode + "?",
            "Confirm Enrollment",
            JOptionPane.YES_NO_OPTION
        );
            if (confirm == JOptionPane.YES_OPTION){
            String result = x.deleteSubj(tid);
            JOptionPane.showMessageDialog(this, result);
            ShowAssignRec(tid);
            } else {
            JOptionPane.showMessageDialog(this, "Assignment canceled.");
        }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid teacher or subject ID!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton8ActionPerformed
    private void loadRecord() {
        DefaultTableModel tblmodel = (DefaultTableModel) jTable2.getModel();
        tblmodel.setRowCount(0);
        
        try {
            ResultSet rs = a.LoadTeacherRecords();
            
            while (rs.next()) {
                String ID = rs.getString("teachID");
                String name = rs.getString("teachName");
                String address = rs.getString("Address");
                String contact = rs.getString("Contact");
                String email = rs.getString("Email");
                String dept = rs.getString("Dept");
                
                String[] data = {ID, name, address, contact, email, dept, };
                
                tblmodel.addRow(data);   
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error fetching regords: " + e.getMessage());
        }
    }
    public void ShowAssignRec(int teachID){
        try{
            ResultSet rs = a.LoadAssignments(teachID);
            DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
            model.setRowCount(0);
            
            while (rs.next()) {
                int id = rs.getInt("subjid");
                String code = rs.getString("SUBcode");
                String desc = rs.getString("SUBdesc");
                String units = rs.getString("SUBUNITS");
                String sched = rs.getString("SUBsched");

            model.addRow(new Object[]{id, code, desc, units, sched});
            }
            rs.close();
            System.out.println("Assignment records loaded successfully!");
        } catch (SQLException e){
            JOptionPane.showMessageDialog(this, "Error loading assignments: " + e.getMessage());
        }
    }
    
